document.addEventListener("DOMContentLoaded", () => {
    const langBtn = document.getElementById('lang-btn');
    const langDropdown = document.getElementById('lang-dropdown');
    const currentLangText = document.getElementById('current-lang');
    const langOptions = document.querySelectorAll('.lang-option');

    // Toggle Dropdown
    if (langBtn) {
        langBtn.addEventListener('click', (e) => {
            e.preventDefault();
            langDropdown.classList.toggle('show');
        });
    }

    // Hide dropdown on click outside
    document.addEventListener('click', (e) => {
        if (langBtn && !langBtn.contains(e.target) && !langDropdown.contains(e.target)) {
            langDropdown.classList.remove('show');
        }
    });

    // Language Change Logic
    langOptions.forEach(option => {
        option.addEventListener('click', (e) => {
            e.preventDefault();
            const selectedLang = e.target.getAttribute('data-lang');
            setLanguage(selectedLang);
            langDropdown.classList.remove('show');

            // Save to local storage
            localStorage.setItem('preferredLang', selectedLang);
        });
    });

    // Initialize Language
    const savedLang = localStorage.getItem('preferredLang') || 'en';
    setLanguage(savedLang);

    function setLanguage(lang) {
        if (!translations[lang]) return;

        // Update Button Text
        let label = 'English';
        if (lang === 'ml') label = 'മലയാളം';
        if (lang === 'ar') label = 'العربية';
        if (currentLangText) currentLangText.innerText = label;

        // Update all elements with data-i18n attribute
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang][key]) {
                el.innerHTML = translations[lang][key];

                // Set RTL for Arabic
                if (lang === 'ar') {
                    // Specific logic to test what it looks like
                    // Right to left adjustments if needed globally.
                }
            }
        });

        // Handle global direction if needed for arabic
        const bodyTag = document.getElementsByTagName('body')[0];
        if (lang === 'ar') {
            bodyTag.style.direction = 'rtl';
            document.documentElement.lang = 'ar';
        } else {
            bodyTag.style.direction = 'ltr';
            document.documentElement.lang = lang;
        }
    }
});
